    <?php
    class Persona
    {

        private $name;
        private $surname;
        private $adress;
        private $comments;
        private $foto;

	function getName(){
        return $this->name;
    }
    function getSurname(){
        return $this->surname;
    }
    function getAdress(){
        return $this->adress;
    }
    function getComments(){
        return $this->comments;
    }
    function getFoto(){
        return $this->foto;
    }
    
    
    
    
    }
	